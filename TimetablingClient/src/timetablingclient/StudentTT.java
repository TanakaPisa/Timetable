    private static java.util.List<org.netbeans.xml.schema.timetable.Event> getMyEvents(java.lang.String arg0, java.lang.String arg1, java.lang.String arg2, java.lang.String arg3) {
        timetablewebservices.Timetabling_Service service = new timetablewebservices.Timetabling_Service();
        timetablewebservices.Timetabling port = service.getTimetablingPort();
        return port.getMyEvents(arg0, arg1, arg2, arg3);
    }

    private static String getStudent(java.lang.String arg0) {
        timetablewebservices.Timetabling_Service service = new timetablewebservices.Timetabling_Service();
        timetablewebservices.Timetabling port = service.getTimetablingPort();
        return port.getStudent(arg0);
    }






    

   

