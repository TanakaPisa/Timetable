    public Date getFirstDayOfWeekDate(int firstDay) {

        Calendar c = new GregorianCalendar();

        while (c.get(Calendar.DAY_OF_WEEK) != firstDay-1) {
            c.add(Calendar.DAY_OF_WEEK, -1);
        }
        return c.getTime();
    }

    public Date getLastDayOfWeekDate(int lastDay) {

        Calendar c = new GregorianCalendar();

        while (c.get(Calendar.DAY_OF_WEEK) != lastDay) {
            c.add(Calendar.DAY_OF_MONTH, -1);
        }
        c.add(Calendar.DAY_OF_WEEK, +5);
        return c.getTime();
    }

    public Date getFirstDayOfNextWeekDate(int firstDay) {

        Calendar c = new GregorianCalendar();

        while (c.get(Calendar.DAY_OF_WEEK) != firstDay-1) {
            c.add(Calendar.DAY_OF_MONTH, +1);
        }
        return c.getTime();
    }

    public Date getLastDayNextOfWeekDate(int lastDay) {

        Calendar c = new GregorianCalendar();

        while (c.get(Calendar.DAY_OF_WEEK) != lastDay) {
            c.add(Calendar.DAY_OF_MONTH, +1);
        }
        c.add(Calendar.DAY_OF_WEEK, +5);
        return c.getTime();
    }

