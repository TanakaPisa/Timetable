    private Event createEvent() {
        Event Package = new Event();
        Package.setCourseId(this.Course_Button.getItemAt(this.Course_Button.getSelectedIndex()));
        Package.setModuleName(this.Module_Button.getItemAt(this.Module_Button.getSelectedIndex()));
        Package.setGroup(this.Group_Button.getItemAt(this.Group_Button.getSelectedIndex()));
        Package.setRoomId(this.Location_Button.getItemAt(this.Location_Button.getSelectedIndex()));
        Package.setType(this.Type_button.getItemAt(this.Type_button.getSelectedIndex()));

        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        String Date = sdf.format(DateChooser.getDate());
        Package.setDate(Date);
        String StartTime = StartTime_Button.getSelectedItem().toString();
        String Duration = Duration_Button.getSelectedItem().toString();
        int TimeSlot1 = Integer.parseInt(StartTime) + Integer.parseInt(Duration);

        Package.setTimeStamp(StartTime_Button.getSelectedItem() + ":00 - " + TimeSlot1 + ":00");
        Package.setTutor(this.StaffAllocation_Button.getSelectedItem().toString());
        //find module id
        List<String> moduleNamesList = getModules(this.Course_Button.getItemAt(this.Course_Button.getSelectedIndex()));
        String modname = this.Module_Button.getItemAt(this.Module_Button.getSelectedIndex());
        int foundIndex = 0;
        for (int x = 0; x < moduleNamesList.size(); x++) {
            if (modname.equals(moduleNamesList.get(x).split("-")[1])) {
                Package.setModuleId(moduleNamesList.get(x).split("-")[0]);
                foundIndex = x;
            }
        }

        List<String> staffNamesList = getFacultyMembers(this.Course_Button.getItemAt(this.Course_Button.getSelectedIndex()));

        for (int y = 0; y < staffNamesList.size(); y++) {
            if (Package.getTutor().equals(staffNamesList.get(y).split("-")[1])) {
                Package.setTutor(staffNamesList.get(y).split("-")[0]);
                break;
            }
        }
        String info = getFacultyMembersInfo(Package.getCourseId(), Package.getTutor());
        Package.setTutorEmail(info.split("-")[0]);
        Package.setTutorOffice(info.split("-")[1]);

        Package.setEventId(this.Course_Button.getSelectedItem().toString() + Package.getModuleId() + this.Type_button.getSelectedItem().toString() + Package.getTimeStamp());

        return Package;
    }

