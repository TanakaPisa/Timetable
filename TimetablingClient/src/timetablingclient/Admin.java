    private void ModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifyActionPerformed

        Boolean repeat = false;
        if ((Course_Button1.getSelectedIndex() > -1) && (Module_Button1.getSelectedIndex() > -1) && (Location_Button1.getSelectedIndex() > -1)) {

            if (DateChooser1.getDate().toString().contains("Sat") || DateChooser1.getDate().toString().contains("Sun")) {
                JOptionPane.showMessageDialog(null, "No classes on saturday or sunday");
                repeat = true;
            } else if (eventsTable.getSelectedRow() > -1) {
                Event a = mttm.getEvent(eventsTable.getSelectedRow());
                mttm.removeEvent(a);
                Event b = modifyEvent();
                repeat = mttm.checkEvent(b);

                if (repeat.equals(false)) {
                    if (Type_button1.getSelectedItem().toString().equals("Lecture")) {

                        b.setGroup("");
                        mttm.addEvent(b);
                        eventsTable.updateUI();
                    } else {
                        mttm.addEvent(b);
                        eventsTable.updateUI();
                    }
                } else {
                    mttm.addEvent(a);
                    eventsTable.updateUI();
                }
            } else {
                JOptionPane.showMessageDialog(this, "No selected row");
                repeat = true;
            }

        } else {
            JOptionPane.showMessageDialog(this, "Dropdowns can not be left empty");

        }
    }//GEN-LAST:event_ModifyActionPerformed

